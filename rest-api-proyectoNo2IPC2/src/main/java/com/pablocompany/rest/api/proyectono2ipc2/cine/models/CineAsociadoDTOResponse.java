//Clase delegada para retornar un listado de llave valor al frontEnd 
public class CineAsociadoDTOResponse {

    private String codigo;
    private String nombre;

    public CineAsociadoDTOResponse(String codigo, String nombre) {
        this.codigo = codigo;
        this.nombre = nombre;
    }

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

}
